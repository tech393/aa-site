# ----------------------------------------------------------- typography
def sm(s):
    """Straight quotes -> smart quotes; -- -> em dash."""
    s = s.replace("---", "—").replace(" -- ", " — ")
    s = re.sub(r'(\w)\'(\w)', '’'.join(['\\1', '\\2']), s)
    s = re.sub(r'(?<=\w)\'', '’', s)
    s = re.sub(r'\'(?=\w)', '‘', s)
    s = s.replace('"', '“', 1) if s.count('"') >= 2 else s
    return s


def esc(s):
    return (s.replace("&", "&amp;").replace("<", "&lt;").replace(">", "&gt;"))


def tidy(s):
    """Escape, then apply smart apostrophes (no HTML in reading text)."""
    s = esc(s)
    s = s.replace("'", "’")
    return s


# ----------------------------------------------------------- parser
def parse_reading(text):
    """Parse reading_text into an ordered list of blocks."""
    blocks = []
    cur = None
    qmode = False

    def flush():
        nonlocal cur
        if cur is not None:
            blocks.append(cur)
        cur = None

    for raw in text.splitlines():
        line = raw.rstrip()
        m = re.match(r'^===\s*(.+?)\s*===\s*$', line.strip())
        if m:
            tag = m.group(1)
            up = tag.upper()
            if up == "QUESTION":
                qmode = True
                continue
            qmode = False
            flush()
            if up.startswith("KEY:"):
                rest = tag[4:].strip()
                if "|" in rest:
                    title, pattern = [p.strip() for p in rest.split("|", 1)]
                else:
                    title, pattern = rest, ""
                cur = {"type": "key", "title": title, "pattern": pattern,
                       "parts": [], "question": ""}
            elif up == "OPENING":
                cur = {"type": "opening", "parts": []}
            elif up == "BLESSING":
                cur = {"type": "blessing", "parts": []}
            elif up == "CLOSE":
                cur = {"type": "close", "parts": []}
            elif up in ("INTRO", "NEXT STEP", "NEXTSTEP"):
                cur = {"type": "skip", "parts": []}   # fixed in template
            else:
                cur = {"type": "opening", "parts": []}
            continue
        if cur is None:
            continue
        if qmode and cur.get("type") == "key":
            if line.strip():
                cur["question"] = (cur["question"] + " " + line.strip()).strip()
            continue
        cur["parts"].append(line)

    flush()
    return blocks


def to_paragraphs(parts):
    """Turn raw lines into [(kind, text)] — kind = 'sub' | 'lead' | 'para'."""
    out = []
    buf = []

    def push():
        if buf:
            txt = " ".join(x.strip() for x in buf).strip()
            if txt:
                if txt.startswith("[[LEAD]]"):
                    out.append(("lead", txt[8:].strip()))
                else:
                    out.append(("para", txt))
        buf.clear()

    for ln in parts:
        s = ln.strip()
        if not s:
            push()
        elif s.startswith("## "):
            push()
            out.append(("sub", s[3:].strip()))
        else:
            buf.append(s)
    push()
    return out


