def _read_request():
    data = request.get_json(silent=True)
    if not data:
        return None, (jsonify({"error": "No JSON body"}), 400)
    for field in ("name", "birth_date", "reading_text"):
        if field not in data:
            return None, (jsonify({"error": f"Missing field: {field}"}), 400)
    return {
        "name": data["name"],
        "birth_date": data.get("birth_date", ""),
        "birth_time": data.get("birth_time", "unknown"),
        "birth_place": data.get("birth_place", ""),
        "reading_text": data["reading_text"],
        "tier": (data.get("tier", "free") or "free").lower(),
    }, None


@app.route("/generate-file", methods=["POST"])
def generate_file():
    """Returns the PDF as a direct file download — use this from Make.com."""
    d, err = _read_request()
    if err:
        return err
    try:
        pdf = build_pdf(d["name"], d["birth_date"], d["birth_time"],
                        d["birth_place"], d["reading_text"], d["tier"])
        slug = d["name"].lower().replace(" ", "-")
        fn = f"dharma-keys-{d['tier']}-reading-{slug}.pdf"
        resp = make_response(pdf)
        resp.headers["Content-Type"] = "application/pdf"
        resp.headers["Content-Disposition"] = f'attachment; filename="{fn}"'
        resp.headers["Content-Length"] = str(len(pdf))
        return resp
    except Exception as e:
        return jsonify({"error": str(e)}), 500


