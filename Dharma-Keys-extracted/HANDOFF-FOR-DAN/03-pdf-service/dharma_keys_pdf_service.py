def render_paras(paras, lead_class="lead", body_class=""):
    html = []
    for kind, txt in paras:
        if kind == "sub":
            html.append(f'<div class="sub">{tidy(txt)}</div>')
        elif kind == "lead":
            html.append(f'<p class="{lead_class}">{tidy(txt)}</p>')
        else:
            cls = f' class="{body_class}"' if body_class else ""
            html.append(f"<p{cls}>{tidy(txt)}</p>")
    return "\n".join(html)


